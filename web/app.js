app.chart = {

    init: function() {

        app.chart.chart = $('#chart');
        app.chart.slider = $( "#slider" );
        app.chart.slider.slider({
            max: app.state.data.length,
            change: app.chart.update
        });
        google.load("visualization", "1", {packages: ["corechart"]});
        google.setOnLoadCallback(app.chart.draw);
    },

    draw: function() {

        var data = google.visualization.arrayToDataTable(app.state.data[app.chart.slider.slider( "value" )]);

        var options = {
            title: 'Correlation between life expectancy, fertility rate and population of some world countries (2010)',
            hAxis: {title: 'Life Expectancy'},
            vAxis: {title: 'Fertility Rate'},
            bubble: {textStyle: {fontSize: 11}}
        };

        var chart = new google.visualization.BubbleChart(document.getElementById('chart'));
        chart.draw(data, options);
    },

    update: function(event, ui) {
        app.chart.draw();
    }

};

app.chart.init();
